    raw = claude(prompt, max_tokens=2000).strip()
    if raw.startswith("```"): raw = "\n".join(raw.splitlines()[1:])
    if raw.endswith("```"):   raw = "\n".join(raw.splitlines()[:-1])
    # Extract JSON object robustly
    start = raw.find("{")
    end   = raw.rfind("}") + 1
