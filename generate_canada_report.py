def q(conn, sql, *args):
    return conn.execute(sql, args).fetchall()

CT = "NATIONAL_TEAMS"

def fetch_zone_data(conn, pid, period, is_dribble):
    rows = q(conn, "SELECT zone,made,missed,total,pct FROM player_shot_zones WHERE player_id=? AND period=? AND is_dribble=? AND competition_type=?", pid, period, is_dribble, CT)
    return {r["zone"]: dict(r) for r in rows}

def fetch_stats(conn, pid, period):
    rows = q(conn, "SELECT stat_label,total,per_game FROM player_stats WHERE player_id=? AND period=? AND competition_type=?", pid, period, CT)
    return {r["stat_label"]: dict(r) for r in rows}

def fetch_play_types(conn, pid, period):
    rows = q(conn, "SELECT play_type,poss,ppp,usage,two_pt_m,two_pt_a,two_pt_pct,three_pt_m,three_pt_a,three_pt_pct,turnovers FROM player_play_types_detail WHERE player_id=? AND period=? AND competition_type=? ORDER BY usage DESC", pid, period, CT)
    return [dict(r) for r in rows if r["usage"] > 0]

def fetch_finishing(conn, pid, period):
    rows = q(conn, "SELECT shot_type,hand,made,attempted,pct FROM player_tendency_finishing WHERE player_id=? AND period=? AND competition_type=? AND attempted>0", pid, period, CT)
    return {(r["shot_type"], r["hand"]): dict(r) for r in rows}

def fetch_turnovers(conn, pid, period):
    rows = q(conn, "SELECT play_type,bad_pass,traveling,total FROM player_turnovers WHERE player_id=? AND period=? AND competition_type=? AND total>0 ORDER BY total DESC", pid, period, CT)
    return [dict(r) for r in rows]

def fetch_shooting_tendency(conn, pid, period):
    rows = q(conn, "SELECT category,hand,two_pt_m,two_pt_a,two_pt_pct FROM player_tendency_shooting WHERE player_id=? AND period=? AND competition_type=?", pid, period, CT)
    return {(r["category"], r["hand"]): dict(r) for r in rows}

